class Offer {
  final String object;
  final String square;
  final String floor;
  final String address;
  final String price;
  final String date;
  final String image;

  Offer({
    required this.object,
    required this.square,
    required this.floor,
    required this.address,
    required this.price,
    required this.date,
    required this.image,
  });
}
